#Q 2: Create a function to check if a number is prime

def is_prime(num):
    if num <= 1:
        return False
    for i in range(2, int(num ** 0.5) + 1):
        if num % i == 0:
            return False
    return True

# Example usage
print(is_prime(7))  # This will print: True
print(is_prime(10)) # This will print: False
